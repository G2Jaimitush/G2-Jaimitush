int sumar(int num1, int num2);
int restar(int num1, int num2);
int multiplicacion(int num1, int num2);
int dividir(int num1, int num2);

int sumar(int num1, int num2) {
    return num1 + num2;
}

int restar(int num1, int num2) {
    return num1 - num2;
}

int multiplicacion(int num1, int num2) {
    return num1 * num2;
}

int dividir(int num1, int num2) {
    return num1 / num2;
}